import { writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const outFile = path.resolve(__dirname, "../js/supabase-config.js");

/** @param {string} supabaseUrl @param {string} supabaseAnonKey */
export function buildSupabaseConfigModuleContents(
	supabaseUrl,
	supabaseAnonKey,
) {
	return `// Generated by scripts/write-supabase-config.mjs — do not edit by hand
export const supabaseUrl = ${JSON.stringify(supabaseUrl)};
export const supabaseAnonKey = ${JSON.stringify(supabaseAnonKey)};
`;
}

function main() {
	const supabaseUrl = process.env.SUPABASE_URL?.trim();
	const supabaseAnonKey = process.env.SUPABASE_ANON_KEY?.trim();

	if (!supabaseUrl || !supabaseAnonKey) {
		console.error(
			"write-supabase-config: set SUPABASE_URL and SUPABASE_ANON_KEY (non-empty).",
		);
		process.exit(1);
	}

	writeFileSync(
		outFile,
		buildSupabaseConfigModuleContents(supabaseUrl, supabaseAnonKey),
		"utf8",
	);
}

const invokedDirectly =
	path.resolve(fileURLToPath(import.meta.url)) ===
	path.resolve(process.argv[1] ?? "");
if (invokedDirectly) {
	main();
}
